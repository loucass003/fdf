#include "fdf.h"

t_vec2		vec2(int x, int y)
{
	t_vec2 p;

	p.x = x;
	p.y = y;
	return (p);
}

t_vec2		vec2_cpy(t_vec2 p)
{
	return (vec2(p.x, p.y));
}