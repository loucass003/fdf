#include "fdf.h"

t_vec2		vec2_mul(t_vec2 p, int x, int y)
{
	p.x *= x;
	p.y *= y;
	return (p);
}

t_vec2		vec2_mulv(t_vec2 p1, t_vec2 p2)
{
	return (vec2_mul(p1, p2.x, p2.y));
}