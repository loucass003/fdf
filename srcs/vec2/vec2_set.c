#include "fdf.h"

t_vec2		*vec2_set(t_vec2 *p, int x, int y)
{
	p->x = x;
	p->y = y;
	return (p);
}

t_vec2		*vec2_setv(t_vec2 *p1, t_vec2 p2)
{
	return (vec2_set(p1, p2.x, p2.y));
}