#include "fdf.h"

t_vec3		vec3(float x, float y, float z)
{
	t_vec3 p;

	p.x = x;
	p.y = y;
	p.z = z;
	return (p);
}

t_vec3		vec3_cpy(t_vec3 p)
{
	return (vec3(p.x, p.y, p.z));
}
